"""jarvis-agent - Pipecat Voice Agent

This bot uses a cascade pipeline: Speech-to-Text → LLM → Text-to-Speech

Generated by Pipecat CLI

Required AI services:
- Qwen (Speech-to-Text)
- Qwen (LLM)
- Qwen (Text-to-Speech)

Run the bot using::

    uv run bot.py
"""

